    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre = jTextField1.getText();
        String apellido = jTextField3.getText();
        String fecha = jTextField4.getText();
        String area = "";
        if (botonYoga.isSelected())
            area = botonYoga.getText();
        if (botonZumba.isSelected())
            area = botonZumba.getText();
        if (botonTrain.isSelected())
            area = botonTrain.getText();
        String horario = "";
        if (seis_nueveAM.isSelected())
            horario = seis_nueveAM.getText();
        if (nueve_doce.isSelected())
            horario = nueve_doce.getText();
        if (doce_tres.isSelected())
            horario = doce_tres.getText();
        if (tres_seis.isSelected())
            horario = tres_seis.getText();
        if (seis_nuevePM.isSelected())
            horario = seis_nuevePM.getText();


        if (nombre.isEmpty() || apellido.isEmpty() || area.isEmpty() || horario.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
            return;
        }

        com.mycompany.models.Asistencias asistencia = isEdition ? edicionAsistencia : new com.mycompany.models.Asistencias();
        asistencia.setArea(area);
        asistencia.setFecha(fecha);
        asistencia.setHorario(horario);
        asistencia.setNombre(nombre);
        asistencia.setApellido(apellido);
        
        

        try{
            DAOAsistencia dao = new DAOAsistenciasImpl();
            if (!isEdition)
                dao.agregar(asistencia);
            else
                dao.modificar(asistencia);
            String mensajeExi = isEdition ? "modificado" : "registrado";
            javax.swing.JOptionPane.showMessageDialog(this,"Asistencia " + mensajeExi + " exitosamente \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
             if (!isEdition){
                jTextField1.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                botonesArea.clearSelection();
                botonesHorario.clearSelection();
            }
            
        } catch (Exception e){
            String mensajeErr = isEdition ? "modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this,"Ocurrio un error al " + mensajeErr + " la asistencia \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

