    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jTable1.getSelectedRow() > -1){
            try {
                int asistenciaID = (int) jTable1.getValueAt(jTable1.getSelectedRow(),0);
                DAOAsistencia dao = new DAOAsistenciasImpl();
                Proyecto_GYM_Instructor.MostrarJPanel(new AgregarAsistencia(dao.obtenerAsistenciaPorID(asistenciaID)));
            }
            catch (Exception e) {
                    Logger.getLogger(Asistencia.class.getName()).log(Level.SEVERE, null, e);
                }
        }
        else {
            javax.swing.JOptionPane.showMessageDialog(this,"Debes seleccionar el equipo a modificar\n", "AVISO",javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


