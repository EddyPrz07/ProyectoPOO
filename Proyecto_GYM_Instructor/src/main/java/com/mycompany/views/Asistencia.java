    private void mostrarAsistencias(){
        try{
            DAOAsistencia dao = new DAOAsistenciasImpl();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            dao.listar("").forEach((u) -> model.addRow(new Object[]{u.getID(),u.getArea(),u.getFecha(),u.getHorario(),u.getNombre(),u.getApellido()}));
            }
            catch (Exception e){
                System.out.println(e.getMessage());
            }
        }
    
