    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String instructor = jTextField1.getText();
        String state = jTextField2.getText();
        boolean estado = Boolean.parseBoolean(state);
        String cup = jTextField5.getText();
        int cupos = Integer.parseInt(cup);
        String area = "";
        if (botonYoga.isSelected())
            area = botonYoga.getText();
        if (botonZumba.isSelected())
            area = botonZumba.getText();
        if (botonTrain.isSelected())
            area = botonTrain.getText();

        String horario = "";
        if (seis_nueveAM.isSelected())
            horario = seis_nueveAM.getText();
        if (nueve_doce.isSelected())
            horario = nueve_doce.getText();
        if (doce_tres.isSelected())
            horario = doce_tres.getText();
        if (tres_seis.isSelected())
            horario = tres_seis.getText();
        if (seis_nuevePM.isSelected())
            horario = seis_nuevePM.getText();
        if (nueve_once.isSelected())
            horario = nueve_once.getText();
        
        
        if (instructor.isEmpty() || area.isEmpty() || horario.isEmpty() || cup.isEmpty() || state.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            jTextField1.requestFocus();
            return;
        }
        
        
        com.mycompany.models.ClasesAdmin clase = claseEdit;
        clase.setInstructor(instructor);
        clase.setEstado(estado);
        clase.setCupos(cupos);
        clase.setArea(area);
        clase.setHorario(horario);
        
        try{
            DAOClasesAdmin dao = new DAOClasesAdminImpl();
            dao.modificar(clase);
            javax.swing.JOptionPane.showMessageDialog(this,"Clase modificada exitosamente \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
        } catch (Exception e){
            javax.swing.JOptionPane.showMessageDialog(this,"Ocurrio un error al modificar la clase \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

