    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String fecha = jTextField9.getText();
        String membresia = "";
        if (botonPlatino.isSelected())
            membresia = botonPlatino.getText();
        if (botonOro.isSelected())
            membresia = botonOro.getText();
        if (botonBronce.isSelected())
            membresia = botonBronce.getText();
        String valor = jTextField10.getText();
        int valoR = Integer.parseInt(valor);
        
        Pagos pago = new Pagos();
        pago.setFecha(fecha);
        pago.setMembresia(membresia);
        pago.setValor(valoR);
        
        if (fecha.isEmpty() || valor.isEmpty() || membresia.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this,"Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            jTextField9.requestFocus();
            return;
        }
        
        try {
            DAOPagos dao = new DAOPagosImpl();
            dao.registrar(pago);
            javax.swing.JOptionPane.showMessageDialog(this,"Pago registrado exitosamente \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            jTextField9.setText("");
            jTextField10.setText("");
            botonesMemb.clearSelection();
        } catch (Exception ex) {
            javax.swing.JOptionPane.showMessageDialog(this,"Ocurrio un error al registrar el pago \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(RegistroPago.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

